# Entrada 
nota1 = int(input('Nota 1: ')) # int() convierte el texto a número entero
nota2 = int(input('Nota 2: '))
nota3 = int(input('Nota 3: '))
nota4 = int(input('Nota 4: '))
# Proceso 
suma = nota1 + nota2 + nota3 + nota4
